package com.velocity;

public class User {
	
	String firstName, lastName, userName, password, city, mailId, mobileNumber;

    public User(String firstName, String lastName, String userName, String password, String city, String mailId, String mobileNumber) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.userName = userName;
        this.password = password;
        this.city = city;
        this.mailId = mailId;
        this.mobileNumber = mobileNumber;
    }

}
